local Tunnel = module("vrp","lib/Tunnel")
local Proxy = module("vrp","lib/Proxy")
vRP = Proxy.getInterface("vRP")

-----------------------------------------------------------------------------------------------------------------------------------------
-- Notify
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterNetEvent("Notify")
AddEventHandler("Notify",function(css,mensagem,timer)
	if timer == nil then
		timer = 3000
	end
	SendNUIMessage({ css = css, mensagem = mensagem, timer = timer })
end)

-----------------------------------------------------------------------------------------------------------------------------------------
-- PBAR
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterNetEvent("progress")
AddEventHandler("progress",function(time,text)
	SendNUIMessage({ type = "progress", display = true, time = time, text = text })
end)